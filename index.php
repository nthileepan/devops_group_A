	<script>
		function startTimer(duration, display) {
			let timer = duration, hours, minutes, seconds;
			setInterval(function () {
				hours = parseInt((timer % (60 * 60 * 24)) / (60 * 60), 10);
				minutes = parseInt((timer % (60 * 60)) / 60, 10);
				seconds = parseInt(timer % 60, 10);

				hours = hours < 10 ? "0" + hours : hours;
				minutes = minutes < 10 ? "0" + minutes : minutes;
				seconds = seconds < 10 ? "0" + seconds : seconds;

				display.textContent = hours + ":" + minutes + ":" + seconds;

				if (--timer < 0) {
					timer = 0;
				}
			}, 1000);
		}

		window.onload = function () {
			const twentyFourHoursInSeconds = 60 * 60 * 24;
			const timer1 = document.querySelector('#timer1');
			const timer2 = document.querySelector('#timer2');
			const timer3 = document.querySelector('#timer3');

			startTimer(twentyFourHoursInSeconds, timer1);
			startTimer(twentyFourHoursInSeconds, timer2);
			startTimer(twentyFourHoursInSeconds, timer3);
		};
